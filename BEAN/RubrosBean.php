<?php


class RubrosBean {

    public $id_rubro;
    public $nomb_rubro;
    public $desc_rubro;
    
    public function getId_rubro() {
        return $this->id_rubro;
    }

    public function getNomb_rubro() {
        return $this->nomb_rubro;
    }

    public function getDesc_rubro() {
        return $this->desc_rubro;
    }

    public function setId_rubro($id_rubro) {
        $this->id_rubro = $id_rubro;
    }

    public function setNomb_rubro($nomb_rubro) {
        $this->nomb_rubro = $nomb_rubro;
    }

    public function setDesc_rubro($desc_rubro) {
        $this->desc_rubro = $desc_rubro;
    }


    
    
}
